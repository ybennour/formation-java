package formes.un;

public class Rectangle {
	
	private int lon;
	private int lar;
	
	public Rectangle(int lon, int lar) {
		this.lon = lon;
		this.lar = lar;
	}

	public int getLon() {
		return lon;
	}

	public void setLon(int lon) {
		this.lon = lon;
	}

	public int getLar() {
		return lar;
	}

	public void setLar(int lar) {
		this.lar = lar;
	}
	
	
}
